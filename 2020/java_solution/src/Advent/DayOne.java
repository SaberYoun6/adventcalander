package Advent;

public class DayOne {

	int Addition(int sum , int data) {
		return sum + data;
}
	int Multiplication(int sum, int data) {
		return data *sum ;
	}
	boolean EqualsTwentyTwenty(int sum) {
		if (sum == 2020) {
			return true;
		} else {
			return false;
		}
	}
	int Additon(int sum, int data, int add) {
		return sum + data + add;
	}
	int multiplication(int sum, int data, int add) {
		return sum * data * add;
	}
}
